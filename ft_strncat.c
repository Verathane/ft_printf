#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (s1[i])
		i++;
	n += i;
	while (*s2 && i < n)
		s1[i++] = *s2++;
	s1[i] = '\0';
	return (s1);
}
