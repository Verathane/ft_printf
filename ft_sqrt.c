int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb < 0)
		return (0);
	if (nb > 10000)
	{
		while (i != nb / 100)
		{
			if (nb == i * i)
				return (i);
			i++;
		}
		return (0);
	}
	while (i != nb)
	{
		if (nb == i * i)
			return (i);
		i++;
	}
	return (0);
}
