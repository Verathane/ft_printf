int		ft_atoi(const char *str)
{
	int s;
	int atoi;

	s = 1;
	atoi = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
		if (*(str++) == '-')
			s = -1;
	while (*str >= '0' && *str <= '9')
		atoi = (atoi * 10) + (s * (*(str++) - 48));
	return (atoi);
}
